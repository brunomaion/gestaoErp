

class Tarefa:
    def __init__(self, x, y):
        self.valor1 = x
        self.valor2 = y
    
    def somar(self):
        return (self.valor1 + self.valor2)
    
    def subtrair(self):
        return (self.valor1 - self.valor2)    
    
    def multiplicar(self):
        return (self.valor1 * self.valor2)      
  
        